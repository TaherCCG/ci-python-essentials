# Storing Values With Assignment Operators

# Variables assignment

first_number = 10
second_number = 5

print("10 + 5")
print(first_number + second_number)


print("first_number = float(10)")
first_number = float(10)

print("10.0 + 5")
print(first_number + second_number)

my_number = 5
my_number = 10
print(my_number)
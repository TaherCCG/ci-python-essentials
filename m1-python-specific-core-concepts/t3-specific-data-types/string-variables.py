#  String Variables

# 1. Single Quotes
# 2. Double Quotes

# Single Quotes
print('Hello')

# Double Quotes
print("Hello")



"""
This will not work

print("Then Mike said "What is that?"")
print('It's a beautiful day')

They will work if you use the escape character \ before the quotes
"""

example = "Then Mike said \"What is that?\""
print(example)

example2 = 'It\'s a beautiful day'
print(example2)


# or you can use as below
example3 = 'Then Mike said "What is that?"'
print(example3)

example4 = "It's a beautiful day"
print(example4)
